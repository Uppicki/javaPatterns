package Pattern4Factory.mod3.classes.PizzaIngredients;

import Pattern4Factory.mod3.interfaces.IPizzaIngredientFactory;

public class NYPizzaIngredientsFactory implements IPizzaIngredientFactory {
    @Override
    public Dough creteDough() {
        return new ThinCrustDough();
    }

    @Override
    public Sauce createSauce() {
        return new MarinaraSauce();
    }

    @Override
    public Cheese createCheese() {
        return new ReggianoChhese();
    }

    @Override
    public Veggies[] createVeggies() {
        return new Veggies[]{new Garlic(), new Onion(), new Mushroom(), new RedPeaper()};
    }

    @Override
    public Pepperoni createPepperoni() {
        return new SlicedPepperoni();
    }

    @Override
    public Clams createClam() {
        return new FreshClams();
    }
}
